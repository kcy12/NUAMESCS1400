import java.util.Scanner;
import java.util.Random;

public class coinFlipper{
   public static void main(String[] args){
         Scanner input = new Scanner(System.in);
         //int vars
         int heads = 0;
         int flips = 0;
         //greet user
         System.out.println("Welcome to the brand new, enhanced with Java, Coin Flipper! Version 2.0!");
         //prompt number of flips
         System.out.println("How many times would you like to flip a coin?");
         //save flips
         flips = input.nextInt();
         //get heads from flipper, seed = 0
         heads = flipper(flips,0);
         //display number of heads and tails
         System.out.println("Here Are Your Results: You Flipped " + heads + " Heads and " + (flips - heads) + " Tails.");
         
   }//end main method
   
   public static int flipper(int nFlips, int seed){
      Random rand = new Random();
      //init vars
      int heads = 0;
      int fCount = 0;
      int coin = 0;
      rand.setSeed(seed);
      //flip a coin
      coin = rand.nextInt(2);
      //while
      while(fCount < nFlips){
         //count flips
         fCount ++;
         //if heads
         if (coin == 0){
            heads ++;
         }//end if
         //flip coin again
         coin = rand.nextInt(2);
      }//end while loop
      return heads;
   }//end flipper method
}//end class




































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































